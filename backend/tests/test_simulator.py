@pytest.mark.asyncio
async def test_add_ticker_idempotent():
    """Adding a ticker that already exists should not change its price."""
    cache = PriceCache()
    source = SimulatorSource()
    await source.start(cache)

    tick_before = await cache.get_latest("AAPL")
    initial_price = tick_before.price

    # Add AAPL again — should be ignored
    source.add_ticker("AAPL")
    await asyncio.sleep(0.1)

    tick_after = await cache.get_latest("AAPL")
    # The price may have changed due to simulation, but the seed price should not have reset
    assert tick_after is not None

    await source.stop()


