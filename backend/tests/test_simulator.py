@pytest.mark.asyncio
async def test_custom_tickers():
    """SimulatorSource can be initialized with a custom ticker list."""
    custom_tickers = [
        TickerConfig(ticker="CUSTOM1", seed_price=100.0),
        TickerConfig(ticker="CUSTOM2", seed_price=200.0),
    ]
    cache = PriceCache()
    source = SimulatorSource(tickers=custom_tickers)
    await source.start(cache)

    tick1 = await cache.get_latest("CUSTOM1")
    tick2 = await cache.get_latest("CUSTOM2")

    assert tick1 is not None
    assert tick2 is not None
    assert tick1.price == 100.0
    assert tick2.price == 200.0

    await source.stop()


@pytest.mark.asyncio
async def test_stop_prevents_further_updates():
    """After stop(), no new price updates should occur."""
    cache = PriceCache()
    source = SimulatorSource()
    await source.start(cache)

    await asyncio.sleep(0.6)  # let at least one tick happen
    await source.stop()

    version_after_stop = cache.version
    await asyncio.sleep(1.0)  # wait for what would have been more ticks

    # Version should not have changed after stop
    assert cache.version == version_after_stop


